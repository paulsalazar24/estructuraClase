
package EstructurasDeDatosEstaticas.insertarValor;

import java.util.Vector;

public class insercionVector {
    public static void main(String[] args) {
        // Declaración y creación de un Vector de tipo Integer
        Vector<Integer> vector = new Vector<>();

        // Inserción de elementos en el Vector
        vector.add(10);  
        vector.add(20);  
        vector.add(30);  

        // Mostrar los elementos del Vector
        System.out.println("Elementos del Vector: " + vector);
    }
}
