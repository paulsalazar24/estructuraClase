
package EstructurasDeDatosEstaticas.insertarValor;

public class insercionArray {
    public static void main(String[] args) {
        // Declaración y creación de un array de enteros con tamaño 3
        int[] array = new int[3];

        // Inserción de elementos en el array
        array[0] = 10;  
        array[1] = 20;  
        array[2] = 30;  

        // Mostrar los elementos del array
        System.out.println("Elemento en la posición 0: " + array[0]);
        System.out.println("Elemento en la posición 1: " + array[1]);
        System.out.println("Elemento en la posición 2: " + array[2]);
    }
    
}
