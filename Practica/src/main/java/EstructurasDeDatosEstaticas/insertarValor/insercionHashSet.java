
package EstructurasDeDatosEstaticas.insertarValor;

import java.util.HashSet;

public class insercionHashSet {
    public static void main(String[] args) {
        // Declaración y creación de un HashSet de tipo String
        HashSet<String> hashSet = new HashSet<>();

        // Inserción de elementos en el HashSet
        hashSet.add("30");  
        hashSet.add("12");  
        hashSet.add("24"); 

        // Mostrar los elementos del HashSet
        System.out.println("Elementos del HashSet: " + hashSet);
    }
}
