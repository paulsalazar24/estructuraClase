
package EstructurasDeDatosEstaticas.insertarValor;

import java.util.LinkedList;

public class insercionLikedList {
    public static void main(String[] args) {
        // Declaración y creación de una LinkedList de tipo String
        LinkedList<String> linkedList = new LinkedList<>();

        // Inserción de elementos en la LinkedList
        linkedList.add("1"); 
        linkedList.add("12");  
        linkedList.add("34");  

        // Mostrar los elementos de la LinkedList
        System.out.println("Elementos de la LinkedList: " + linkedList);
    }
}
