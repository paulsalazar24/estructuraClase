
package EstructurasDeDatosEstaticas.funciones;

public class comparacionIgualdad {
    public static void main(String[] args) {
        //Definimos 2 cadenas de caracteres con el mismo contenido
        String cadena1 = "Hola";
        String cadena2 = "Hola";
        
        //Comparación de igualdad utilizando el operador ==
        boolean sonIguales = (cadena1 == cadena2);
        
        //Imprimimos el resultado de la comparación
        System.out.println("Las cadenas son iguales?" + sonIguales);
    }
    
}
